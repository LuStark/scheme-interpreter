int FindMaxIndex( int A[], int n )
{
    int max_i;
    int max;

    int i;

    max = -1;
    max_i = 0;
    for( i=0; i<n; i++ )
    {
        if( A[i]>max )
        {
            max = A[i];
            max_i = i;
        }
    }
    return max_i;
}

